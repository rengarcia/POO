    private void txtTerm1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTerm1KeyTyped
        char validate=evt.getKeyChar();
        
        if (Character.isLetter(validate)){
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(rootPane, "The input is not valid");
            LOG.warning("Values entered incorrect");
        }
    }//GEN-LAST:event_txtTerm1KeyTyped

