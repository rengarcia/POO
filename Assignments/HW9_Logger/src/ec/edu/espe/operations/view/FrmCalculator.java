    private void jBtnMultiplicationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnMultiplicationActionPerformed
        jLblResult.setText("");
        int factorOne;
        int factorTwo;
        int result;
        
        factorOne = Integer.parseInt(txtTerm1.getText());
        factorTwo = Integer.parseInt(txtTerm2.getText());
        result = Operations.multiply(factorOne, factorTwo);
        jLblResult.setText(String.valueOf(result));
        
        
    }//GEN-LAST:event_jBtnMultiplicationActionPerformed

